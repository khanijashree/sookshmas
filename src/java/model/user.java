
package model;

public class user {

    String id;
    String pw;
    String name;
    String phone;
    String email;
    String branch;

    public String getId() {
        return id;
    }

    public String getname() {
        return name;
    }

    public String getbranch() {
        return branch;
    }

    public String getphone() {
        return phone;
    }
    
     public String getemail() {
        return email;
    }
     
     public String getpw() {
        return pw;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setname(String name) {
        this.name = name;
    }
    
    public void setbranch(String branch) {
        this.branch = branch;
    }

    public void setphone(String phone) {
        this.phone = phone;
    }

    public void setemail(String email) {
        this.email = email;
    }

     public void setpw(String pw) {
        this.pw = pw;
    }
}







